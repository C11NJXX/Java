package C_11nJxx.date_3_30.polymorphism;

public class Person {
    private String name;
    private int age;

    public Person() {
    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    //keepPet方法,利用多态
    public void keepPet(Animal animal,String fodder){
        animal.eat(fodder);
    }
}
